#ifndef	_ARCH_HW_CCI_400_H_
#define	_ARCH_HW_CCI_400_H_


#define CCI400_CTLR_OVERRIDE	0x0000
#define CCI400_SPEC_CTLR	0x0004
#define CCI400_SECURE_ACCESS	0x0008
#define CCI400_STATUS		0x000c
#define CCI400_STATUS_PENDING		(1 << 0)

#define CCI400_IMPRECISE_ERR	0x0010

#define CCI400_PMCR		0x0100

#define CCI400_PID4		0x0FD0
#define CCI400_PID5		0x0FD4
#define CCI400_PID6		0x0FD8
#define CCI400_PID7		0x0FDC
#define CCI400_PID0		0x0FE0
#define CCI400_PID1		0x0FE4
#define CCI400_PID2		0x0FE8
#define CCI400_PID3		0x0FEC
#define CCI400_CID0		0x0FF0
#define CCI400_CID1		0x0FF4
#define CCI400_CID2		0x0FF8
#define CCI400_CID3		0x0FFC

#define	CCI400_SLAVE_IF(N)	(0x1000 * (N))

#define	CCI400_SNOOP_CTRL(N)	(0x1000 + CCI400_SLAVE_IF(N))
#define	CCI400_DVM_SUPPORTED		(1 << 31)
#define	CCI400_SNOOP_SUPPORTED		(1 << 30)
#define	CCI400_DVM_EN			(2 << 0)
#define	CCI400_SNOOP_EN			(1 << 0)
#define	CCI400_ALL_ENABLE		(CCI400_SNOOP_EN|CCI400_DVM_EN)

#endif
